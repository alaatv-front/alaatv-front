<template>
  <div v-if="localOptions.sliderItems"
       class="row">
    <div class="col-1 q-pl-xl arrow-right">
      <q-btn icon="isax:arrow-right-1"
             color="primary"
             size="xl"
             @click="goToRight" />
    </div>
    <div class="col-10">
      <q-virtual-scroll ref="virtualScroll"
                        v-slot="{ item, index }"
                        :items="localOptions.sliderItems"
                        class="student-scroll-bar"
                        virtual-scroll-horizontal>
        <q-card :key="index"
                class="scroll-item-card">
          <q-img :src="item.image"
                 width="160px"
                 height="160px"
                 spinner-color="primary"
                 class="student-img"
                 spinner-size="82px">
            <div v-if="localOptions.personType === 'student'"
                 class="student-major"
                 :class="{'riazi': item.major === 'ریاضی', 'tajrobi': item.major === 'تجربی'}">
              {{ item.major }}
            </div>
          </q-img>
          <q-card-section class="person-name-card-section">
            <div class="student-name ellipsis-2-lines">{{ item.first_name + ' ' + item.last_name }}</div>
          </q-card-section>
          <q-card-section class="person-info-card-section">
            <div v-if="localOptions.personType === 'student'"
                 class="student-info">
              <div class="rank">
                {{ item.rank }}
              </div>
              <div class="region">
                {{ item.distraction === '1' ? 'منطقه یک' : item.distraction === '2' ? 'منطقه دو' : item.distraction === '3' ? 'منطقه سه' : item.distraction}}
              </div>
            </div>
            <div v-if="localOptions.personType === 'teacher'"
                 class="teacher-info">
              <div class="major">
                {{ item.major }}
              </div>
            </div>
          </q-card-section>
        </q-card>
      </q-virtual-scroll>
    </div>
    <div class="arrow-left col-1 text-right q-pr-xl">
      <q-btn icon="isax:arrow-left"
             color="primary"
             size="xl"
             @click="goToLeft" />
    </div>
  </div>
  <div v-else
       class="loading">
    ...
  </div>
</template>

<script>
import { mixinWidget } from 'src/mixin/Mixins.js'

export default {
  name: 'PersonSlider',
  mixins: [mixinWidget],
  data() {
    return {
      defaultOptions: {
        sliderItems: [],
        personType: 'student'
      },
      scrollIndex: 0
    }
  },
  mounted() {
    this.init()
  },
  methods: {
    onVirtualScroll({ index }) {
      this.scrollIndex = index
    },
    goToRight() {
      if (this.scrollIndex > 0) {
        this.scrollIndex -= 1
        this.$refs.virtualScroll.scrollTo(this.scrollIndex)
      }
    },
    goToLeft() {
      if (this.scrollIndex < this.localOptions.sliderItems.length) {
        this.scrollIndex += 1
        this.$refs.virtualScroll.scrollTo(this.scrollIndex)
      }
    },
    init() {
      if (this.$refs.virtualScroll) {
        setInterval(() => {
          this.scrollIndex += 1
          if (this.scrollIndex > this.localOptions.sliderItems.length) {
            this.scrollIndex = 0
          }
          this.$refs.virtualScroll.scrollTo(this.scrollIndex)
        }, 2000)
      }
    }
  }
}
</script>

